package com.adobe.cq.commerce.magento.graphql;

import java.util.Map;

import com.google.gson.JsonElement;
import com.google.gson.JsonObject;
import com.shopify.graphql.support.AbstractResponse;
import com.shopify.graphql.support.SchemaViolationError;

public class ProductReviewRating extends AbstractResponse<ProductReviewRating> {
    public ProductReviewRating() {
    }

    public ProductReviewRating(JsonObject fields) throws SchemaViolationError {
        for (Map.Entry<String, JsonElement> field : fields.entrySet()) {
            String key = field.getKey();
            String fieldName = getFieldName(key);
            switch (fieldName) {
                case "name": {
                    responseData.put(key, jsonAsString(field.getValue(), key));

                    break;
                }

                case "value": {
                    responseData.put(key, jsonAsString(field.getValue(), key));

                    break;
                }

                case "__typename": {
                    responseData.put(key, jsonAsString(field.getValue(), key));
                    break;
                }

                default: {
                    readCustomField(fieldName, field.getValue());
                }
            }
        }
    }

    public String getGraphQlTypeName() {
        return "ProductReviewRating";
    }

    /**
     * The label assigned to an aspect of a product that is being rated, such as quality or price
     */
    public String getName() {
        return (String) get("name");
    }

    public ProductReviewRating setName(String arg) {
        optimisticData.put(getKey("name"), arg);
        return this;
    }

    /**
     * The rating value given by customer. By default, possible values range from 1 to 5.
     */
    public String getValue() {
        return (String) get("value");
    }

    public ProductReviewRating setValue(String arg) {
        optimisticData.put(getKey("value"), arg);
        return this;
    }

    public boolean unwrapsToObject(String key) {
        switch (getFieldName(key)) {
            case "name": return false;

            case "value": return false;

            default: return false;
        }
    }
}
